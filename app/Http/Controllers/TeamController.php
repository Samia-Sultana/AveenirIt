    {
        $input = $request->all();
        Team::create([
            'member' => $input['name'],
            'designation' => $input['designation'],
            'priority' => $input['priority'],

        ]);
        $notification = array(
            'message' => 'New Member added!',
            'alert-type' => 'success'
        );
        return redirect()->route('addTeamPage')->with($notification);
    }

